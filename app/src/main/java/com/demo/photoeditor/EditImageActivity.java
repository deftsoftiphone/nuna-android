    private String createNewFile() {

        File file = new File(Environment.getExternalStorageDirectory()
                + File.separator + ""
                + System.currentTimeMillis() + ".jpeg");
        try {
            file.createNewFile();
        } catch (IOException e) {

        }
        return file.getAbsolutePath();
    }

    @SuppressLint("MissingPermission")
    private void saveImage() {
        if (requestPermission(Manifest.permission.WRITE_EXTERNAL_STORAGE)) {
            showLoading("Processing...");
            File file = new File(Environment.getExternalStorageDirectory()
                    + File.separator + ""
                    + System.currentTimeMillis() + ".jpeg");
            try {
                file.createNewFile();

                SaveSettings saveSettings = new SaveSettings.Builder()
                        .setClearViewsEnabled(true)
                        .setTransparencyEnabled(false)
                        .build();

                mPhotoEditor.saveAsFile(file.getAbsolutePath(), saveSettings, new PhotoEditor.OnSaveListener() {
                    @Override
                    public void onSuccess(@NonNull String imagePath) {
                        hideLoading();
                        //showSnackbar("Image Saved Successfully");
                        mPhotoEditorView.getSource().setImageURI(Uri.fromFile(new File(imagePath)));

                        // startActivity(new Intent(EditImageActivity.this, CreatePostFragment.class).putExtra("strfile",imagePath));
                        //     finish();
                        if (iSmultipleImage) {
                            Intent resultIntent = new Intent();
                            resultIntent.putExtra("strfile", imagePath);
                            resultIntent.putExtra("isVideo", false);
                            resultIntent.putExtra("pos", pos);
                            setResult(Activity.RESULT_OK, resultIntent);
                            finish();

                        } else {
                            Intent resultIntent = new Intent();
                            resultIntent.putExtra("strfile", imagePath);
                            resultIntent.putExtra("isVideo", false);
                            setResult(Activity.RESULT_OK, resultIntent);
                            finish();
                        }

                    }

                    @Override
                    public void onFailure(@NonNull Exception exception) {
                        hideLoading();
                        showSnackbar("Failed to save Image");
                    }
                });
            } catch (IOException e) {
                e.printStackTrace();
                hideLoading();
                showSnackbar(Objects.requireNonNull(e.getMessage()));
            }
        }
    }

    @Override
    protected void onActivityResult(int requestCode, int resultCode, Intent data) {
        super.onActivityResult(requestCode, resultCode, data);
        if (resultCode == RESULT_OK) {
            switch (requestCode) {
                case ACTION_REQUEST_EDITIMAGE:
                    mPhotoEditor.clearAllViews();

                    String newFilePath = data.getStringExtra("output_path");
                    File imgFile = new File(newFilePath);
                    if (imgFile.exists()) {
                        Bitmap myBitmap = BitmapFactory.decodeFile(imgFile.getAbsolutePath());
                        mPhotoEditorView.getSource().setImageBitmap(myBitmap);
                    }
                    break;

                case CAMERA_REQUEST:
                    mPhotoEditor.clearAllViews();
                    Bitmap photo = (Bitmap) data.getExtras().get("data");
                    mPhotoEditorView.getSource().setImageBitmap(photo);
                    break;
                case PICK_REQUEST:
                    try {
                        mPhotoEditor.clearAllViews();
                        Uri uri = data.getData();
                        Bitmap bitmap = MediaStore.Images.Media.getBitmap(getContentResolver(), uri);
                        mPhotoEditorView.getSource().setImageBitmap(bitmap);
                    } catch (IOException e) {
                        e.printStackTrace();
                    }
                    break;
            }
        }
    }

